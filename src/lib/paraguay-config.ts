// Funciones de utilidad para Paraguay
export const paraguayUtils = {
  // Validar formato de teléfono paraguayo
  validatePhone: (phone: string): boolean => {
    const phoneRegex = /^\+595\s?\d{2,3}\s?\d{3}\s?\d{3}$/;
    return phoneRegex.test(phone);
  },
  
  // Formatear teléfono paraguayo
  formatPhone: (phone: string): string => {
    // Remover todos los caracteres no numéricos excepto el +
    const cleaned = phone.replace(/[^\d+]/g, '');
    
    // Si no empieza con +595, agregarlo
    if (!cleaned.startsWith('+595')) {
      return `+595 ${cleaned}`;
    }
    
    // Formatear con espacios
    const number = cleaned.replace('+595', '').replace(/\s/g, '');
    if (number.length === 9) {
      return `+595 ${number.slice(0, 3)} ${number.slice(3, 6)} ${number.slice(6)}`;
    }
    
    return cleaned;
  },
  
  // Generar teléfono de ejemplo
  generateExamplePhone: (): string => {
    // Sin datos ficticios: no generar teléfonos "de ejemplo" en runtime
    return '';
  },
  
  // Obtener ciudades por estado
  getCitiesByState: (state: string) => {
    return paraguayConfig.cities.filter(city => city.state === state);
  },
  
  // Obtener barrios de Asunción
  getAsuncionNeighborhoods: () => {
    return paraguayConfig.asuncionNeighborhoods;
  },
  
  // Generar nombre completo paraguayo
  generateFullName: (): { firstName: string; lastName: string } => {
    // Sin datos ficticios: no generar nombres "de ejemplo" en runtime
    return { firstName: '', lastName: '' };
  }
}; 